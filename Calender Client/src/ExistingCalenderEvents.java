import com.google.gdata.data.DateTime;


public class ExistingCalenderEvents {

	String CalenderEventTitle;
	DateTime startTime;
	DateTime endTime;
	
	public String getCalenderEventTitle() {
		return CalenderEventTitle;
	}
	public void setCalenderEventTitle(String calenderEventTitle) {
		CalenderEventTitle = calenderEventTitle;
	}
	public DateTime getStartTime() {
		return startTime;
	}
	public void setStartTime(DateTime startTime) {
		this.startTime = startTime;
	}
	public DateTime getEndTime() {
		return endTime;
	}
	public void setEndTime(DateTime endTime) {
		this.endTime = endTime;
	}
	
	
	
	
	
}

