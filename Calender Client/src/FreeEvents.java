import com.google.gdata.data.DateTime;


public class FreeEvents {
	DateTime freeStartTime;
	DateTime freeEndTime;
	DateTime dateOfFreeEvent;
	
	public DateTime getFreeStartTime() {
		return freeStartTime;
	}
	public void setFreeStartTime(DateTime freeStartTime) {
		this.freeStartTime = freeStartTime;
	}
	public DateTime getFreeEndTime() {
		return freeEndTime;
	}
	public void setFreeEndTime(DateTime freeEndTime) {
		this.freeEndTime = freeEndTime;
	}
	public DateTime getDateOfFreeEvent() {
		return dateOfFreeEvent;
	}
	public void setDateOfFreeEvent(DateTime dateOfFreeEvent) {
		this.dateOfFreeEvent = dateOfFreeEvent;
	}

	
	
}
